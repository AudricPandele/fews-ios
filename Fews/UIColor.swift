import UIKit

extension UIColor {
    class func FewsBlueColor() -> UIColor {
        return UIColor(red: 0.08, green: 0.38, blue: 0.96, alpha: 1)
    }
    
    class func FewsGreyColor() -> UIColor {
        return UIColor(red: 0.86, green: 0.86, blue: 0.86, alpha: 1)
    }
    
    class func FewsGreyBlackColor() -> UIColor {
        return UIColor(red: 0.14, green: 0.14, blue: 0.14, alpha: 1)
    }
}